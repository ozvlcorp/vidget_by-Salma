export function t(lang: Lang, key: string): string {
  return translations[lang]?.[key] ?? translations.ru[key] ?? key
}

export function parseLang(raw: string | null): Lang {
  if (raw === 'ru' || raw === 'en' || raw === 'kk' || raw === 'uz') return raw
  return 'ru'
}
